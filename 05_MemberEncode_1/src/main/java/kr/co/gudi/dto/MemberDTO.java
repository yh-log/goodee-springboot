package kr.co.gudi.dto;

public class MemberDTO {

	private String id;
	private String pw;
	private String name;
	private int age;
	private String gender;
	private String email;
	String getId() {
		return id;
	}
	void setId(String id) {
		this.id = id;
	}
	String getPw() {
		return pw;
	}
	void setPw(String pw) {
		this.pw = pw;
	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	int getAge() {
		return age;
	}
	void setAge(int age) {
		this.age = age;
	}
	String getGender() {
		return gender;
	}
	void setGender(String gender) {
		this.gender = gender;
	}
	String getEmail() {
		return email;
	}
	void setEmail(String email) {
		this.email = email;
	}
	
	
	
	
	
}
